const detectBrowserLanguage = (): SupportedLanguage | null => {
  if (typeof navigator === "undefined") return null;

  // Get browser languages in order of preference
  const browserLangs = navigator.languages || [navigator.language];

  for (const browserLang of browserLangs) {
    // Extract language code (e.g., 'fr-FR' -> 'fr', 'en-US' -> 'en')
    const langCode = browserLang.toLowerCase().split("-")[0];

    // Map common language codes to our supported languages
    const langMap: Record<string, SupportedLanguage> = {
      fr: "fr",
      en: "en",
      es: "es",
      de: "de",
      it: "it",
      pt: "pt",
      ar: "ar",
      ko: "ko",
      ja: "ja",
      ru: "ru",
    };

    const mappedLang = langMap[langCode];
    if (mappedLang && SUPPORTED_LANGUAGES.find((l) => l.code === mappedLang)) {
      console.log(
        `[i18n] Browser language detected: ${mappedLang} (from ${browserLang})`,
      );
      return mappedLang;
    }
  }

  return null;
};

const detectLocationLanguage = async (): Promise<SupportedLanguage | null> => {
  try {
    const response = await fetch("https://ipapi.co/json/", {
      signal: AbortSignal.timeout(3000),
    });
    if (!response.ok) return null;
    const data = await response.json();
    const countryCode = data.country_code?.toUpperCase();
    if (countryCode && COUNTRY_TO_LANGUAGE[countryCode]) {
      return COUNTRY_TO_LANGUAGE[countryCode];
    }
  } catch (e) {
    console.warn("Could not detect location language:", e);
  }
  return null;
};

const getStoredOrDetectedLanguage = (): SupportedLanguage => {
  if (typeof window === "undefined" || typeof localStorage === "undefined") {
    return "en";
  }

  // 1. Check if user has manually selected a language
  try {
    const stored = localStorage.getItem("cstream-language");
    if (stored) {
      const parsed = JSON.parse(stored);
      if (
        parsed?.state?.language &&
        SUPPORTED_LANGUAGES.find((l) => l.code === parsed.state.language)
      ) {
        console.log(`[i18n] Using stored language from zustand: ${parsed.state.language}`);
        return parsed.state.language;
      }
    }

    // Fallback to simpler key if zustand key is missing or invalid
    const simpleStored = localStorage.getItem("cstream_language") as SupportedLanguage;
    if (simpleStored && SUPPORTED_LANGUAGES.find(l => l.code === simpleStored)) {
      return simpleStored;
    }
  } catch (e) {
    console.warn("Could not read stored language:", e);
  }

  // 2. Auto-detect from browser
  const browserLang = detectBrowserLanguage();
  if (browserLang) {
    return browserLang;
  }

  // 3. Default to English
  console.log("[i18n] Using default language: en");
  return "en";
};

export const autoDetectAndSetLanguage = async () => {
  // Only auto-detect if no language has been manually set
  const hasManualSelection = localStorage.getItem("cstream-language-manual");
  if (hasManualSelection) {
    console.log(
      "[i18n] Manual language selection detected, skipping auto-detection",
    );
    return;
  }

  const detectedLang = detectBrowserLanguage();
  if (detectedLang) {
    const currentLang = useI18n.getState().language;
    if (currentLang !== detectedLang) {
      console.log(`[i18n] Auto-setting language to: ${detectedLang}`);
      useI18n.getState().setLanguage(detectedLang);
    }
  }
};

const initialLanguage = getStoredOrDetectedLanguage();

// Helper to get native name safely
export const getLanguageNativeName = (code: string): string => {
  const lang = SUPPORTED_LANGUAGES.find((l) => l.code === code);
  if (!lang) return code;
  return lang.nativeName || (lang as any).native || code;
};

export const useI18n = create<I18nState>()(
  persist(
    (set, get) => ({
      language: initialLanguage,
      setLanguage: (lang: SupportedLanguage, isManual: boolean = false) => {
        if (typeof window !== 'undefined') {
          localStorage.setItem("cstream_language", lang);
          // Always mark as manual when user explicitly changes language
          // This prevents auto-detection from overriding their preference
          localStorage.setItem("cstream-language-manual", "true");
        }
        set({ language: lang });
        const isRtl = SUPPORTED_LANGUAGES.find((l) => l.code === lang)?.rtl;
        document.documentElement.lang = lang;
        document.documentElement.dir = isRtl ? "rtl" : "ltr";

        if (isManual) {
          console.log(`[i18n] Language manually set to: ${lang}`);
        }
      },
      t: (key: TranslationKey) => {
        const lang = get().language;
        return (
          translations[lang]?.[key] ||
          translations.en[key] ||
          translations.fr[key] ||
          key
        );
      },
      getTMDBLanguage: () => {
        return TMDB_LANGUAGE_MAP[get().language] || "en-US";
      },
      isRTL: () => {
        return (
          SUPPORTED_LANGUAGES.find((l) => l.code === get().language)?.rtl ||
          false
        );
      },
    }),
    {
      name: "cstream-language",
    },
  ),
);

export const initializeI18n = () => {
  const state = useI18n.getState();
  const isRtl = SUPPORTED_LANGUAGES.find((l) => l.code === state.language)?.rtl;
  document.documentElement.lang = state.language;
  document.documentElement.dir = isRtl ? "rtl" : "ltr";

  // Auto-detect language on first load
  autoDetectAndSetLanguage();
};

export const useLanguageStore = useI18n;

export const t = (
  key: TranslationKey,
  language?: SupportedLanguage,
): string => {
  const lang = language || useI18n.getState().language;
  return (
    translations[lang]?.[key] ||
    translations.en[key] ||
    translations.fr[key] ||
    key
  );
};
